# WRITE FIND_DUPLICATES FUNCTION HERE #
def find_duplicates(nums):
    seen = dict()
    result = []
    for num in nums:
        if num not in seen:
            seen[num] = 1
        elif num not in result:
            result.append(num)
    return result
#######################################




